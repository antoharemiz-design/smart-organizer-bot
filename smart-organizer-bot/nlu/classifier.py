"""
Быстрый классификатор интентов на основе правил.
Определяет, является ли сообщение задачей, вопросом или болтовнёй.
"""
import re

# Глаголы создания задачи (максимально широкий список)
TASK_VERBS = [
    # Прямые глаголы действия
    "позвонить", "купить", "сделать", "приготовить", "сходить", "съездить",
    "написать", "отправить", "забрать", "встретиться", "позвать",
    "зайти", "посетить", "проверить", "оплатить", "заказать",
    "помыть", "убрать", "постирать", "погладить", "починить",
    "пойти", "поехать", "слетать", "прийти", "приехать",
    "начать", "закончить", "доделать", "выполнить", "завершить",
    "посмотреть", "прочитать", "изучить", "выучить", "нарисовать",
    "погулять", "пробежать", "потренироваться", "позаниматься",

    # Глаголы планирования
    "напомни", "напомнить", "запланируй", "запланировать", "добавь", "добавить",
    "создай", "создать", "поставь", "поставить", "запиши", "записать",
    "внеси", "внести", "отметь", "отметить", "занеси", "занести",

    # Задачи с существительными
    "задача", "дело", "план", "событие", "встреча", "звонок",
    "дедлайн", "срок", "поручение",
]

# Вопросительные слова
QUESTION_WORDS = [
    "что", "какие", "когда", "сколько", "покажи", "список",
    "задачи", "планы", "расскажи", "какой", "какая", "какое",
    "где", "куда", "зачем", "почему",
]

# Временные маркеры (если есть — скорее задача)
TIME_MARKERS = [
    "завтра", "сегодня", "послезавтра", "через час", "через полчаса",
    "утром", "днём", "вечером", "ночью", "в обед",
    "в понедельник", "во вторник", "в среду", "в четверг",
    "в пятницу", "в субботу", "в воскресенье",
    "каждый день", "каждую неделю", "каждый месяц",
    "в январе", "в феврале", "в марте", "в апреле", "в мае", "в июне",
    "в июле", "в августе", "в сентябре", "в октябре", "в ноябре", "в декабре",
]


def classify(text: str) -> str:
    """Определяет интент сообщения: task, question или chitchat."""
    text_lower = text.lower().strip()

    # Проверяем вопросительные предложения
    if re.match(r'^(что|какие|когда|сколько|покажи|список|расскажи|какой|какая|какое|где|куда|зачем|почему)\b', text_lower):
        # Если есть явный глагол задачи — это задача
        if any(verb in text_lower for verb in TASK_VERBS[:15]):
            return "task"
        return "question"

    # Если есть глагол задачи — однозначно задача
    if any(verb in text_lower for verb in TASK_VERBS):
        return "task"

    # Если есть временной маркер — скорее задача
    if any(marker in text_lower for marker in TIME_MARKERS):
        return "task"

    # Проверяем на числа (время: "в 7", "к 19", "19:00")
    if re.search(r'[кв]\s+\d{1,2}', text_lower) or re.search(r'\d{1,2}:\d{2}', text_lower):
        return "task"

    # Если сообщение короткое (приветствие, спасибо) — chitchat
    if len(text_lower) < 5:
        return "chitchat"

    # По умолчанию — задача (пользователь скорее пишет задачу, чем болтает)
    return "task"